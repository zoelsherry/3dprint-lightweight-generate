int MarchingCubes::AddTriangleIndices(const double v[Cube::CORNERS], double iso, int* isoIndices) { return AddTriangleIndices(GetIndex(v, iso), isoIndices); }
int MarchingCubes::AddTriangleIndices(int idx, int* isoIndices)
{
	int ntriang = 0;

	/* Cube is entirely in/out of the surface */
	if (!edgeMask[idx]) return 0;

	/* Create the triangle */
	for (int i = 0; triangles[idx][i] != -1; i += 3) {
		for (int j = 0; j<3; j++) { isoIndices[i + j] = triangles[idx][i + j]; }
		ntriang++;
	}
	return ntriang;
}

